package com.ps.model;

public class weaponModel {
    int weapon_id;
    String weapon_name;
    String weapon_type;
    String weapon_serial;
    String weapon_quantity;
    String weapon_issue_date;

    public int getWeapon_id() {
        return weapon_id;
    }

    public void setWeapon_id(int weapon_id) {
        this.weapon_id = weapon_id;
    }

    public String getWeapon_name() {
        return weapon_name;
    }

    public void setWeapon_name(String weapon_name) {
        this.weapon_name = weapon_name;
    }

    public String getWeapon_type() {
        return weapon_type;
    }

    public void setWeapon_type(String weapon_type) {
        this.weapon_type = weapon_type;
    }

    public String getWeapon_serial() {
        return weapon_serial;
    }

    public void setWeapon_serial(String weapon_serial) {
        this.weapon_serial = weapon_serial;
    }

    public String getWeapon_quantity() {
        return weapon_quantity;
    }

    public void setWeapon_quantity(String weapon_quantity) {
        this.weapon_quantity = weapon_quantity;
    }

    public String getWeapon_issue_date() {
        return weapon_issue_date;
    }

    public void setWeapon_issue_date(String weapon_issue_date) {
        this.weapon_issue_date = weapon_issue_date;
    }
    
    
    
    
}
