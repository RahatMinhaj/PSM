
package com.ps.model;

public class CaseTrialModel {
    private String case_id;
    private String court_name;
    private String court_facing_date;

    
    
    public String getCase_id() {
        return case_id;
    }

    public void setCase_id(String case_id) {
        this.case_id = case_id;
    }

    public String getCourt_name() {
        return court_name;
    }

    public void setCourt_name(String court_name) {
        this.court_name = court_name;
    }

    public String getCourt_facing_date() {
        return court_facing_date;
    }

    public void setCourt_facing_date(String court_facing_date) {
        this.court_facing_date = court_facing_date;
    }
    
    
    
    
    
}
